from scipy.io import loadmat

def load_mnist():
    path = "./mfml/resources/mnist.mat"
    raw = loadmat(path)
    mnist = {
        "data": raw["data"].T,
        "target": raw["label"][0],
        "COL_NAMES": ["label", "data"],
        "DESCR": "mldata.org dataset: mnist-original",
    }
    return mnist


